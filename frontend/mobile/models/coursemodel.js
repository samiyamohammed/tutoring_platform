export class Course {
  constructor(id, title, poster, rating, image, progress, coursestatus, students, tutor) {
    this.id = id;
    this.title = title;
    this.poster = poster;
    this.rating = rating;
    this.image = image;
    this.progress = progress;
    this.coursestatus = coursestatus;
    this.students = students;
    this.tutor = tutor; 
  }
}
